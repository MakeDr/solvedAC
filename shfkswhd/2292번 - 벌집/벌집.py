# 벌집 문제 수학적 풀이:
# 
# 1. n층의 마지막 번호 도출:
#    1층: 1, 2층: 7, 3층: 19, ...
#    n층: 1 + 6*(1+2+...+(n-1)) = 1 + 6*(n-1)*n/2 = 3n(n-1) + 1
# 
# 2. N이 속한 층 찾기:
#    3(n-1)(n-2) + 1 < N ≤ 3n(n-1) + 1
# 
# 3. 부등식 변형:
#    (n-1)(n-2) < (N-1)/3 ≤ n(n-1)
# 
# 4. 근사해 도출:
#    n(n-1) ≈ n² 이므로 n ≈ √((N-1)/3)
#    실제로는 n ≈ √((N-1)/3) + 1 (보정)
# 
# 5. 최종 공식:
#    n = round(√((N-1)/3)) + 1
# 
# 원라이너: print(round(((int(input())-1)/3)**0.5)+1)
N = int(input())
if((N-2)<0):
    print(1)
else:
    P = (N-2)//3
    for i in range(0,int(N**0.5)+1):
        if(i*(i+1) > P):
            print(i+1)
            break